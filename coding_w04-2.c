#include <stdio.h>                //เพิ่ม # หน้า include และเพิ่ม <> ไว้หน้าและหลังของ stdio.h

int main() {            
    int a = 10;                   // กำหนด a=10 และเพิ่ม ; ท้ายประโยค
    printf("a = %d",a);           //แสดงตัวแปร a และเพิ่ม ; ท้ายประโยค

    return 0;                     //จบการทำงานและเพิ่ม ; ท้ายประโยค
}